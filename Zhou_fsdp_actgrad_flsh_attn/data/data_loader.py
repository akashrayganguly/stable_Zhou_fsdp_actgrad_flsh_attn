# ============================================================================
# FSDP-Compatible Data Provider Function
# ============================================================================

def data_provider(args, flag):
    """
    Factory function to create dataset and dataloader with FSDP support.

    CRITICAL FSDP RULES:
    1. When using DistributedSampler, DataLoader MUST have shuffle=False
    2. DistributedSampler handles shuffling via its own shuffle parameter
    3. Training: shuffle=True in sampler, shuffle=False in DataLoader
    4. Val/Test: shuffle=False in both sampler and DataLoader

    Args:
        args: Argument object containing configuration
        flag: 'train', 'val', 'test', or 'pred'

    Returns:
        data_set: Dataset instance
        data_loader: DataLoader instance (with DistributedSampler if using FSDP)
    """
    # Dataset configuration
    Data = data_dict[args.data]
    timeenc = 0 if args.embed != 'timeF' else 1

    # Determine batch size and shuffle behavior
    if flag == 'test':
        shuffle_flag = False  # Never shuffle test data
        drop_last = True
        batch_size = args.batch_size
        freq = args.freq
    elif flag == 'pred':
        shuffle_flag = False  # Never shuffle prediction data
        drop_last = False
        batch_size = 1
        freq = args.detail_freq
        Data = Dataset_Pred
    else:
        # Training and validation
        shuffle_flag = True if flag == 'train' else False
        drop_last = True
        batch_size = args.batch_size
        freq = args.freq

    # Create dataset
    data_set = Data(
        root_path=args.root_path,
        data_path=args.data_path,
        flag=flag,
        size=[args.seq_len, args.label_len, args.pred_len],
        features=args.features,
        target=args.target,
        inverse=args.inverse,
        timeenc=timeenc,
        freq=freq,
        cols=args.cols
    )

    # Create dataloader with FSDP support
    if hasattr(args, 'use_fsdp') and args.use_fsdp:
        # ===================================================================
        # FSDP MODE: Use DistributedSampler
        # ===================================================================
        sampler = DistributedSampler(
            data_set,
            shuffle=shuffle_flag,  # Sampler handles shuffling
            seed=args.seed if hasattr(args, 'seed') else 2021,
            drop_last=drop_last
        )

        data_loader = DataLoader(
            data_set,
            batch_size=batch_size,
            shuffle=False,  # âœ… CRITICAL: Must be False when using sampler!
            sampler=sampler,
            num_workers=args.num_workers,
            drop_last=drop_last,
            pin_memory=True,  # Recommended for FSDP to speed up GPU transfers
            persistent_workers=True if args.num_workers > 0 else False
        )
    else:
        # ===================================================================
        # STANDARD MODE: Regular DataLoader
        # ===================================================================
        data_loader = DataLoader(
            data_set,
            batch_size=batch_size,
            shuffle=shuffle_flag,  # DataLoader handles shuffling
            num_workers=args.num_workers,
            drop_last=drop_last
        )

    return data_set, data_loader


